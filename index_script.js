    function getDisplayCategory(item){
      const raw = String(item.category || "").trim().toLowerCase();
      if(raw === "plant") return "Plant Assets";
      if(raw === "people" || raw === "person") return "People Assets";
      return item.category || "Uncategorized";
    }

    function getSearchableText(item){
      const parts = [
        item.category || "",
        item.title || "",
        ...(item.tags || []),
        ...(item.desc || [])
      ];
      return parts.join(" | ").toLowerCase();
    }

    function matchesCategoryFilter(item, category){
      if(category === "All") return true;
      const keywords = categoryKeywordMap[category] || [String(category).toLowerCase()];
      const haystack = getSearchableText(item);
      return keywords.some(keyword => haystack.includes(String(keyword).toLowerCase()));
    }

